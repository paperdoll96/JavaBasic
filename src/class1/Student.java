package class1;

/*
    사람이 관리하기 좋은 방식은 '학생' 이라는 개념을 하나로 묶는것이다!
    그리고 각각의 학생 별로 본인의 이름, 나이, 성적을 관리하는 것이다!
    앞서 이야기한 문제를 클래스를 도입해서 해결해보자!
 */
public class Student {
    String name;
    int age;
    int grade;

    /*
         class  키워드를 사용해서 학생 클래스(Student)를 정의한다!
         학생 클래스는 내부에 이름(name), 나이(age), 성적(grade) 변수를 가진다
         이렇게 클래스에 정의한 변수들을 멤버변수, 또는 필드라고 한다!
         ※ 자바에서 멤버 변수, 필드는 같은 뜻이다. 클래스에 소속된 변수를 뜻한다.
     */

}
